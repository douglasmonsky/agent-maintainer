POST_EDIT_HOOK = textwrap.dedent(
    """\
    \"\"\"Codex PostToolUse hook: run fast package maintenance checks.\"\"\"

    from __future__ import annotations

    import json
    import os
    import subprocess  # nosec B404
    import sys
    from pathlib import Path

    sys.dont_write_bytecode = True


    def verifier_python(repo_root: Path) -> str:
        for relative in (".venv/bin/python", "venv/bin/python"):
            candidate = repo_root / relative
            if candidate.exists():
                return str(candidate)
        return sys.executable


    def verifier_env(repo_root: Path) -> dict[str, str]:
        env = dict(os.environ)
        env.setdefault("PYTHONDONTWRITEBYTECODE", "1")
        src_path = str(repo_root / "src")
        pythonpath = env.get("PYTHONPATH")
        env["PYTHONPATH"] = f"{src_path}{os.pathsep}{pythonpath}" if pythonpath else src_path
        return env


    def main() -> int:
        repo_root = Path(__file__).resolve().parents[2]
        command = [
            verifier_python(repo_root),
            "-m",
            "agent_maintainer",
            "verify",
            "--profile",
            "fast",
            "--base-ref",
            "HEAD",
        ]
        result = subprocess.run(
            command,
            cwd=repo_root,
            env=verifier_env(repo_root),
            text=True,
            capture_output=True,
            check=False,
        )
        if result.returncode == 0:
            return 0
        output = (result.stdout or result.stderr or "Verification failed.").strip()
        print(
            json.dumps(
                {
                    "decision": "block",
                    "reason": "Fast repository checks failed.",
                    "hookSpecificOutput": {
                        "hookEventName": "PostToolUse",
                        "additionalContext": output[:6000],
                    },
                }
            )
        )
        return 0


    if __name__ == "__main__":
        sys.exit(main())
    """
)

STOP_HOOK = textwrap.dedent(
    '''\
    """Codex Stop hook: run precommit Agent Maintainer checks."""

    from __future__ import annotations

    import json
    import os
    import subprocess  # nosec B404
    import sys
    from pathlib import Path

    sys.dont_write_bytecode = True


    def verifier_python(repo_root: Path) -> str:
        for relative in (".venv/bin/python", "venv/bin/python"):
            candidate = repo_root / relative
            if candidate.exists():
                return str(candidate)
        return sys.executable


    def verifier_env(repo_root: Path) -> dict[str, str]:
        env = dict(os.environ)
        env.setdefault("PYTHONDONTWRITEBYTECODE", "1")
        src_path = str(repo_root / "src")
        pythonpath = env.get("PYTHONPATH")
        env["PYTHONPATH"] = f"{src_path}{os.pathsep}{pythonpath}" if pythonpath else src_path
        return env


    def emit(payload: dict[str, object]) -> int:
        print(json.dumps(payload))
        return 0


    def main() -> int:
        try:
            payload = json.load(sys.stdin)
        except json.JSONDecodeError:
            payload = {}

        if payload.get("stop_hook_active") is True:
            return emit({"continue": True})

        repo_root = Path(__file__).resolve().parents[2]
        command = [
            verifier_python(repo_root),
            "-m",
            "agent_maintainer",
            "verify",
            "--profile",
            "precommit",
            "--base-ref",
            "HEAD",
        ]
        result = subprocess.run(
            command,
            cwd=repo_root,
            env=verifier_env(repo_root),
            text=True,
            capture_output=True,
            check=False,
        )
        if result.returncode == 0:
            return emit({"continue": True})

        output = (result.stdout or result.stderr or "Verification failed.").strip()
        return emit(
            {
                "decision": "block",
                "reason": (
                    "Final verification failed. Fix issues before finishing."
                    f"{chr(10) * 2}{output[:8000]}"
                ),
            }
        )


    if __name__ == "__main__":
        sys.exit(main())
    '''
)

