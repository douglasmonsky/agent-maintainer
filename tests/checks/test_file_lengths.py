def test_file_length_can_write_baseline(tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
    monkeypatch.chdir(tmp_path)
    target = tmp_path / "legacy.py"
    baseline = tmp_path / "file-length-baseline.json"
    target.write_text("a = 1\nb = 2\n", encoding="utf-8")

    result = check_file_lengths.main(
        ["legacy.py", "--max-physical", "1", "--write-baseline", str(baseline)]
    )

    assert result == 0
    payload = json.loads(baseline.read_text(encoding="utf-8"))
    assert payload["files"] == {"legacy.py": {"physical": 2, "source": 2}}
